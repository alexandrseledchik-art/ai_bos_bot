function buildNeutralLeadOverloadQuestion(text) {
  const normalized = normalizeText(text).toLowerCase();
  const warmInbound = /т[её]пл|входящ/i.test(normalized);

  if (/не\s+успева|обработ/i.test(normalized)) {
    return warmInbound
      ? "Возьми последние 20 тёплых входящих и скажи по факту: сколько из них продавец мог взять в работу сразу, а сколько ему всё равно пришлось руками квалифицировать и решать, стоит ли вести дальше?"
      : "Возьми последние 20 входящих и скажи по факту: сколько из них дошли до продавца уже с понятным приоритетом, а сколько команда сначала руками разбирала и решала, кто вообще целевой?";
  }

  if (/перегруж|тонут|захлеб/i.test(normalized)) {
    return warmInbound
      ? "По последним тёплым обращениям что видно в фактах: продавец берёт их в работу сразу или сначала сам руками квалифицирует и отделяет реальных клиентов от шума?"
      : "По последним обращениям что видно в фактах: продавцы получают уже отфильтрованный поток или сначала руками сортируют, кого считать целевым и кому отвечать первым?";
  }

  return warmInbound
    ? "Сейчас мне нужен один наблюдаемый факт: по тёплому входу продавец видит уже отобранный поток или сначала сам руками квалифицирует и приоритизирует лидов?"
    : "Сейчас мне нужен один наблюдаемый факт: до продавца лид уже приходит отфильтрованным и с приоритетом или команда сначала вручную разбирает поток и решает, кто вообще целевой?";
}

function buildEntryState(context, focus, signalSufficiency, selectedConstraint = "", promotionReadiness = "keep_in_entry") {
  const text = normalizeText(context.userText);
  const previous = context.entryState || {};
  const explicitClaimedCause = extractClaimedCause(text);
  const claimedCause = explicitClaimedCause || (
    shouldCarryPreviousClaimedCause(text)
      ? normalizeText(previous.claimedCause)
      : ""
  );
  const graphConstraints = constraintsFromGraphPacket(context.graphPacket);
  const shouldPreservePreviousSpread = Boolean(context.classification?.inferredFollowUp) || isMetaFollowUpText(text);
  let candidateConstraints = uniqueConstraints(
    shouldPreservePreviousSpread
      ? [
          ...(previous.candidateConstraints || []),
          ...graphConstraints,
          ...genericConstraintsByFocus(focus, text, context.graphPacket)
        ]
      : [
          ...graphConstraints,
          ...genericConstraintsByFocus(focus, text, context.graphPacket),
          ...(previous.candidateConstraints || [])
        ]
  );
  let nextBestQuestion = buildNextQuestion(focus, text, candidateConstraints, context.graphPacket);

  if (shouldPreservePreviousSpread && isMetaFollowUpText(text) && previous.nextBestQuestion) {
    nextBestQuestion = normalizeText(previous.nextBestQuestion);
  }

  if (context.classification?.type === "free_text_problem") {
    const specificConstraints = candidateConstraints.filter((item) => !isGenericPlaceholderConstraintLabel(item.label));
    if (specificConstraints.length >= 3) {
      candidateConstraints = uniqueConstraints(specificConstraints, 5);
    }
  }

  if (
    /заяв|лид|входящ/.test(text.toLowerCase()) &&
    /не усп|обработ|перегруж|тонут|захлеб/i.test(text.toLowerCase()) &&
    !/не\s+хватает\s+(люд|продав|менеджер)|нехватк/i.test(text.toLowerCase()) &&
    !/icp|квалификац|предквалификац|приоритет|всё подряд|смешан|неразобран|целев/i.test(text.toLowerCase())
  ) {
    nextBestQuestion = buildNeutralLeadOverloadQuestion(text);
  }

  if (
    /заяв|лид|входящ/.test(text.toLowerCase()) &&
    /целевыми|с кем вообще стоит работать/i.test(nextBestQuestion)
  ) {
    nextBestQuestion = "По последним 20 входящим: лид уже приходит с понятным приоритетом и статусом «целевой/нецелевой», или продавцы сами разбирают всё подряд?";
  }

  const symptoms = uniqueStrings([
    ...(previous.symptoms || []),
    text
  ]);

  const knownFacts = uniqueStrings([
    ...(previous.knownFacts || []),
    claimedCause ? `Пользователь сам считает причиной: ${claimedCause}.` : ""
  ], 8);

  return {
    entryMode: context.classification?.entryMode || "unclear",
    claimedProblem:
      context.classification?.type === "free_text_problem" || context.classification?.type === "url_plus_problem"
        ? text
        : normalizeText(previous.claimedProblem || text),
    claimedCause,
    knownFacts,
    symptoms,
    observedSignals: uniqueStrings([
      ...(previous.observedSignals || []),
      ...(context.graphPacket?.observedSignals || [])
    ], 12),
    systemLayers: inferSystemLayers(text, focus, candidateConstraints, previous.systemLayers || []),
    candidateConstraints,
    candidateStates: context.graphPacket?.candidateStates || previous.candidateStates || [],
    candidateCauses: context.graphPacket?.candidateCauses || previous.candidateCauses || [],
    selectedConstraint,
    graphTrace: context.graphPacket?.graphTrace || previous.graphTrace || [],
    discriminatingSignals: context.graphPacket?.discriminatingSignals || previous.discriminatingSignals || [],
    graphConfidence: Number(context.graphPacket?.graphConfidence ?? previous.graphConfidence ?? 0),
    hypothesisConflicts: uniqueStrings([
      ...(previous.hypothesisConflicts || []),
      ...(context.graphPacket?.hypothesisConflicts || [])
    ], 6),
    signalSufficiency,
    nextBestQuestion,
    nextBestStep: promotionReadiness === "ready_for_diagnostic_case"
      ? `Зафиксировать ограничение "${selectedConstraint}" и проверить его по одному узлу данных или процесса.`
      : "Сначала отделить локальную жалобу от системного ограничения.",
    whyThisStep:
      promotionReadiness === "ready_for_diagnostic_case"
        ? "Теперь есть достаточно сигнала, чтобы перейти от версии к рабочему ограничению и первой волне действий."
        : "Этот шаг лучше всего разделяет конкурирующие причины и не даёт принять ближайшее объяснение за истину.",
    promotionReadiness
  };
}

