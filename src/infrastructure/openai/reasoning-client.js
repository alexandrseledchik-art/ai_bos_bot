function normalizeText(value) {
  return String(value || "").trim();
}

function uniqueStrings(items, maxItems = 10) {
  return [...new Set((items || []).map((item) => normalizeText(item)).filter(Boolean))].slice(0, maxItems);
}

function uniqueConstraints(items, maxItems = 5) {
  const result = [];
  const seen = new Set();

  for (const item of items || []) {
    const label = normalizeText(item?.label);
    if (!label) {
      continue;
    }

    const key = label.toLowerCase();
    if (seen.has(key)) {
      continue;
    }
    seen.add(key);

    result.push({
      label,
      layer: normalizeText(item?.layer || "management").toLowerCase(),
      confidence: Math.max(0, Math.min(1, Number(item?.confidence ?? 0.5))),
      whyPossible: normalizeText(item?.whyPossible),
      whatWouldDisprove: normalizeText(item?.whatWouldDisprove)
    });

    if (result.length >= maxItems) {
      break;
    }
  }

  return result;
}

function isGenericPlaceholderConstraintLabel(label) {
  const normalized = normalizeText(label).toLowerCase();
  return /пользователь видит локальную боль|сло[^,]*, который пока не назван прямо/.test(normalized);
}

function detectFocus(text) {
  const normalized = normalizeText(text).toLowerCase();

  if (/продать бизнес|продаж[ау]\s+бизнес|подготов.*к\s+продаж[еуы]\s+бизнес|exit|выход\s+из\s+бизнес|выйти\s+из\s+операц/.test(normalized)) {
    return "sale";
  }
  if (/прибыл|марж|деньг|касс/.test(normalized)) {
    return "profit";
  }
  if (/команд|операц|хаос|собственник|процесс|регламент|управл/.test(normalized)) {
    return "operations";
  }
  if (/рост|лид|трафик|воронк|продаж|заяв/.test(normalized)) {
    return "growth";
  }
  return "general";
}

function extractClaimedCause(text) {
  const normalized = normalizeText(text);
  const patterns = [
    /проблема в\s+([^,.!?]+)/i,
    /не хватает\s+([^,.!?]+)/i,
    /из-за\s+([^,.!?]+)/i,
    /потому что\s+([^,.!?]+)/i
  ];

  for (const pattern of patterns) {
    const match = normalized.match(pattern);
    if (match?.[1]) {
      if (/не хватает/i.test(pattern.source)) {
        return `нехватка ${normalizeText(match[1])}`;
      }
      return normalizeText(match[1]);
    }
  }

  if (/лиды плох/i.test(normalized)) {
    return "лиды плохого качества";
  }
  if (/не\s+справля|не\s+успева|перегруж|тонут/i.test(normalized)) {
    return "";
  }
  if (/люд[а-я]* не хватает/i.test(normalized)) {
    return "не хватает людей";
  }
  if (/продавц[а-я]*\s+не\s+хватает/i.test(normalized)) {
    return "не хватает продавцов";
  }
  if (/менеджер[а-я]*\s+не\s+хватает/i.test(normalized)) {
    return "не хватает менеджеров";
  }
  if (/долго отвеча/i.test(normalized)) {
    return "слишком долгий первый ответ";
  }

  return "";
}

function detectHardSignal(text) {
  const normalized = normalizeText(text);
  return /\d|%|₽|руб|mrr|cac|ltv/i.test(normalized) ||
    /(марж|конверс).*(упал|просел|сниз|вырос|с\s+\S+\s+до\s+\S+)/i.test(normalized);
}

function isMetaFollowUpText(text) {
  const normalized = normalizeText(text).toLowerCase();
  return /что ты имеешь в виду|что именно ты имеешь в виду|в смысле|почему мы идем именно сюда|почему ид[её]м сюда|ок[, ]*а дальше|что дальше|и дальше|что потом|я не уверен|сомневаюсь|не думаю|не похоже/i.test(
    normalized
  );
}

function shouldCarryPreviousClaimedCause(text) {
  return isMetaFollowUpText(text);
}

